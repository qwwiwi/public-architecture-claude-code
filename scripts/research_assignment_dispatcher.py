#!/usr/bin/env python3
"""
Research Assignment Dispatcher

Runs curiosity_loop then dispatches tasks to HOMER and tracks completion.
- Writes task brief files to workspaces/homer/inbox/<task_id>.md
- Watches workspaces/homer/task-ledger.md for status changes
- On completion (status DONE), updates gap to insight_stored
"""
import subprocess
import re
import time
from datetime import datetime
from pathlib import Path

# Paths
BASE = Path(__file__).parent.parent
WORKSPACE_BASE = BASE.parent.parent / "workspaces" / "homer"
GAPS = WORKSPACE_BASE / "memory" / "curiosity-gaps.md"
LEDGER = WORKSPACE_BASE / "task-ledger.md"
INBOX = WORKSPACE_BASE / "inbox"

SEP = "\n---\n"
KV_RE = re.compile(r"^\s*([\w-]+)\s*[:=]\s*(.*)$")


def parse_ledger() -> list[dict]:
    """Parse task-ledger.md into list of task dicts."""
    if not LEDGER.exists():
        return []

    blocks = LEDGER.read_text(encoding="utf-8").split(SEP)
    tasks = []

    for block in blocks:
        if not block.strip():
            continue

        data = {}
        for ln in block.splitlines():
            m = KV_RE.match(ln)
            if m:
                data[m.group(1).lower()] = m.group(2).strip()

        if "id" in data or "task_id" in data:
            tasks.append(data)

    return tasks


def write_inbox_brief(task: dict) -> Path:
    """Write research brief to inbox."""
    INBOX.mkdir(parents=True, exist_ok=True)

    tid = task.get("id") or task.get("task_id", "unk")
    path = INBOX / f"{tid}.md"

    title = task.get("title", "(no title)")
    assignee = task.get("assignee", "HOMER")
    source_gap = task.get("source_gap", "?")
    created = task.get("created_at", datetime.utcnow().isoformat())

    brief = f"""# Research Brief: {title}

**Task ID:** {tid}  
**Assigned:** {assignee}  
**Source Gap:** {source_gap}  
**Created:** {created}

---

## Objective
Research the identified gap and provide findings with actionable recommendations.

## Context Capsule

### Goal
Deliver research findings for gap `{source_gap}` to enable decision-making.

### Confirmed Facts
- Gap documented in curiosity-gaps.md
- Owner: {assignee}
- Research approach defined in source gap

### Active Artifacts
- Task ledger: {LEDGER}
- Curiosity gaps: {GAPS}

### Next Step
1. Research the gap using available tools and data
2. Store findings in memory ({WORKSPACE_BASE}/memory/)
3. Update task status to DONE in task-ledger.md
4. Gap status will auto-update to insight_stored

## Deliverable
Research report stored in memory with clear findings and recommendations.

---
*Auto-generated by research_assignment_dispatcher*
"""
    path.write_text(brief, encoding="utf-8")
    return path


def update_gap_to_insight(gap_id: str) -> bool:
    """Update gap status to insight_stored in curiosity-gaps.md."""
    if not GAPS.exists():
        return False

    txt = GAPS.read_text(encoding="utf-8")
    parts = txt.split(SEP)
    updated = False
    out = []

    for part in parts:
        if not part.strip():
            continue

        # Check if this block contains the gap_id
        if gap_id in part:
            lines = part.splitlines()
            new_lines = []
            seen_status = False

            for ln in lines:
                m = KV_RE.match(ln)
                if m and m.group(1).lower() == "status":
                    new_lines.append("status: insight_stored")
                    seen_status = True
                else:
                    new_lines.append(ln)

            if not seen_status:
                new_lines.append("status: insight_stored")

            out.append("\n".join(new_lines))
            updated = True
        else:
            out.append(part)

    if updated:
        GAPS.write_text(SEP.join(out), encoding="utf-8")
        return True
    return False


def run_curiosity_loop() -> None:
    """Execute curiosity_loop.py."""
    script_path = Path(__file__).parent / "curiosity_loop.py"
    result = subprocess.run(
        ["python3", str(script_path)],
        capture_output=True,
        text=True,
        check=False,
    )
    print(result.stdout)
    if result.stderr:
        print(f"[dispatcher] stderr: {result.stderr}", file=__import__("sys").stderr)


def main(poll_interval: int = 5, timeout: int = 300) -> None:
    """
    Main dispatcher loop.
    Runs curiosity_loop, dispatches tasks, tracks completion.
    """
    print("[dispatcher] Starting research assignment dispatcher...")

    # Run curiosity loop
    run_curiosity_loop()

    # Parse ledger for open HOMER tasks
    tasks = parse_ledger()
    assigned = [
        t for t in tasks
        if t.get("assignee", "").upper() == "HOMER"
        and t.get("status", "").upper() == "OPEN"
    ]

    if not assigned:
        print("[dispatcher] No new tasks to dispatch.")
        return

    # Dispatch tasks by creating inbox briefs
    for task in assigned:
        path = write_inbox_brief(task)
        print(f"[dispatcher] Dispatched {task.get('id')} -> {path}")

    # Track completion
    print(f"[dispatcher] Tracking completion (timeout={timeout}s)...")
    start = time.time()
    completed_gaps = set()

    while time.time() - start < timeout:
        tasks = parse_ledger()

        for task in tasks:
            sid = task.get("source_gap", "")
            assignee = task.get("assignee", "").upper()
            status = task.get("status", "").upper()

            if (
                assignee == "HOMER"
                and status in ("DONE", "COMPLETED")
                and sid
                and sid not in completed_gaps
            ):
                print(f"[dispatcher] Task {task.get('id')} completed — updating gap {sid}")
                if update_gap_to_insight(sid):
                    completed_gaps.add(sid)
                    print(f"[dispatcher] Gap {sid} updated to insight_stored")

        # Exit if all tracked gaps are updated
        if len(completed_gaps) == len(assigned):
            break

        time.sleep(poll_interval)

    print(f"[dispatcher] Finished. Updated {len(completed_gaps)} gap(s).")


if __name__ == "__main__":
    import sys

    # Allow poll_interval and timeout from args
    poll = int(sys.argv[1]) if len(sys.argv) > 1 else 5
    tout = int(sys.argv[2]) if len(sys.argv) > 2 else 300
    main(poll, tout)